package com.umerqureshicodes.backend.s3;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import software.amazon.awssdk.auth.credentials.DefaultCredentialsProvider;
import software.amazon.awssdk.regions.Region;
import software.amazon.awssdk.services.s3.S3Client;
import software.amazon.awssdk.services.s3.presigner.S3Presigner;

@Configuration
public class S3Config {

    @Value("${aws.s3.region}")
    private String region;

    // Creates a reusable S3Client bean for uploading/deleting objects.
    // Uses DefaultCredentialsProvider which auto-discovers credentials from
    // ~/.aws/credentials, env vars, IAM roles, etc. — no hardcoded keys needed.
    @Bean
    public S3Client s3Client() {
        return S3Client.builder()
                .region(Region.of(region))
                .credentialsProvider(DefaultCredentialsProvider.create())
                .build();
    }

    // Creates a reusable S3Presigner bean for generating presigned URLs
    // (presigned URLs let the frontend fetch private S3 images without needing AWS credentials)
    @Bean
    public S3Presigner s3Presigner() {
        return S3Presigner.builder()
                .region(Region.of(region))
                .credentialsProvider(DefaultCredentialsProvider.create())
                .build();
    }
}
